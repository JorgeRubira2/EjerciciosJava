package com.jorgerubira.ejerciciosjava.pojo;

public class Ciudad {
    private String nombre;
    private int personas;

    public Ciudad(String nombre, int personas) {
        this.nombre = nombre;
        this.personas = personas;
    }

    public String getNombre() {
        return nombre;
    }

    public int getPersonas() {
        return personas;
    }
    
    
}
